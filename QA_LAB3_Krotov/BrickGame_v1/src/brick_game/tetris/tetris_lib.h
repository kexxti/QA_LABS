#ifndef TETRIS_LIB_H
#define TETRIS_LIB_H

#include <stdbool.h>
#include <stdlib.h>
#include <sys/time.h>

#include "defines.h"

/**
 * @brief Перечисление действий пользователя
 *
 * Содержит все возможные действия, которые может совершить пользователь
 * во время игры.
 */
typedef enum {
  Start,     /**< Начать игру */
  Pause,     /**< Поставить на паузу / снять с паузы */
  Terminate, /**< Завершить игру */
  Left,      /**< Движение влево */
  Right,     /**< Движение вправо */
  Up,        /**< Движение вверх (не используется) */
  Down,      /**< Движение вниз (ускорить падение) */
  Action     /**< Повернуть фигуру */
} UserAction_t;

/**
 * @brief Основная информация о состоянии игры
 *
 * Структура передаётся в GUI для отрисовки текущего состояния.
 */
typedef struct {
  int **field;      /**< Игровое поле HEIGHT×WIDTH (1 — заполнено, 0 — пусто) */
  int **next;       /**< Следующая фигура: 4 блока + код вращения (5×2) */
  int score;        /**< Текущий счёт */
  int high_score;   /**< Рекордный счёт (загружается из файла) */
  int level;        /**< Текущий уровень (1–10) */
  int speed;        /**< Скорость (не используется напрямую, вычисляется по уровню) */
  int pause;        /**< Состояние паузы: 0 — игра, 1 — пауза, 2 — конец игры, 3 — выход */
} GameInfo_t;

/**
 * @brief Перечисление состояний конечного автомата
 */
typedef enum {
  Begin,     /**< Начальное состояние — ожидание старта */
  Spawn,     /**< Создание новой фигуры */
  Moving,    /**< Обработка ввода и движение фигуры */
  Shifting,  /**< Автоматическое падение фигуры по таймеру */
  Attaching, /**< Присоединение фигуры к полю */
  GameOver,  /**< Конец игры */
  Break      /**< Пауза */
} State;

/**
 * @brief Описание текущей падающей фигуры
 */
typedef struct {
  int start_pos[2];     /**< Позиция якоря фигуры [строка, столбец] */
  int blocks_pos[4][2]; /**< Относительные координаты 4 блоков фигуры */
  int code_rotate;      /**< Размер матрицы вращения (2–4) */
} Figure;

/**
 * @brief Полное состояние игры (единственный статический экземпляр)
 */
typedef struct {
  GameInfo_t game_info;    /**< Игровая информация для GUI */
  State state;             /**< Текущее состояние FSM */
  UserAction_t user_action;/**< Последнее действие пользователя */
  Figure figure;           /**< Текущая падающая фигура */
  int button_pressed;      /**< Флаг нажатия кнопки */
  unsigned long long time; /**< Временная метка для таймера падения (мс) */
} FullInfo;

/**
 * @brief Возвращает указатель на статический объект состояния игры
 * @return Указатель на единственный экземпляр FullInfo
 */
FullInfo *get_current_state();

/**
 * @brief Сохраняет пользовательский ввод в состоянии игры
 * @param action Действие пользователя
 * @param hold Флаг удержания кнопки (true — кнопка нажата)
 */
void userInput(UserAction_t action, bool hold);

/**
 * @brief Возвращает текущую игровую информацию
 * @return Копия структуры GameInfo_t с текущим состоянием поля, счётом и уровнем
 */
GameInfo_t updateCurrentState();

/**
 * @brief Основная функция конечного автомата — обрабатывает текущее состояние
 */
void sigact();

#endif  // TETRIS_LIB_H
